"""Utility functions for my_package.

This module contains common utility functions for mathematical operations.
"""


def add(a: int | float, b: int | float) -> int | float:
    """Add two numbers together.
    
    Args:
        a: The first number.
        b: The second number.
    
    Returns:
        The sum of a and b.
    """
    return a + b


def multiply(a: int | float, b: int | float) -> int | float:
    """Multiply two numbers together.
    
    Args:
        a: The first number.
        b: The second number.
    
    Returns:
        The product of a and b.
    """
    return a * b
